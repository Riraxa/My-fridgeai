/**
 * 食材名を正規化キーに変換する
 * 1. 前後の空白除去
 * 2. 小文字化
 * 3. 全角英数字→半角
 * 4. カタカナ→ひらがな（またはその逆）
 * 5. 既知の別名テーブル参照
 * 6. 記号・括弧の除去
 */
export function normalizeIngredientKey(rawName: string): string {
  if (!rawName || typeof rawName !== "string") {
    return "";
  }

  // 1. 前後の空白除去
  let normalized = rawName.trim();

  // 2. 小文字化（英字部分のみ）
  normalized = normalized.toLowerCase();

  // 3. 全角→半角変換
  normalized = toHalfWidth(normalized);

  // 4. カタカナ→ひらがな変換
  normalized = katakanaToHiragana(normalized);

  // 5. 既知の別名テーブル参照
  const aliased = INGREDIENT_ALIASES[normalized];
  if (aliased) {
    return aliased;
  }

  // 6. 記号・括弧の除去（最低限のクリーニング）
  normalized = normalized
    .replace(/[\uff08\uff09\u0028\u0029]/g, "") // （）()
    .replace(/[\u3000\s]+/g, ""); // スペース・全角スペース除去

  return normalized;
}

/**
 * 全角→半角変換
 */
function toHalfWidth(str: string): string {
  return (
    str
      // 全角英数字→半角
      .replace(/[\uff10-\uff19\uff21-\uff3a\uff41-\uff5a]/g, (ch) =>
        String.fromCharCode(ch.charCodeAt(0) - 0xfee0),
      )
  );
}

/**
 * カタカナ→ひらがな変換
 */
function katakanaToHiragana(str: string): string {
  return str.replace(/[\u30a1-\u30f6]/g, (ch) =>
    String.fromCharCode(ch.charCodeAt(0) - 0x60),
  );
}

/**
 * 複数の食材名を一括正規化
 */
export function normalizeIngredientKeys(rawNames: string[]): string[] {
  return rawNames.map((name) => normalizeIngredientKey(name));
}

/**
 * 正規化キーが同一食材かどうか判定（デバッグ用）
 */
export function areSameIngredient(name1: string, name2: string): boolean {
  return normalizeIngredientKey(name1) === normalizeIngredientKey(name2);
}

