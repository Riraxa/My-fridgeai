// 数量抽出パターン
export const QUANTITY_PATTERNS = [
  // ml, L
  { regex: /(\d+(?:\.\d+)?)\s*(ml|mL|ミリリットル)/i, unit: "ml" },
  { regex: /(\d+(?:\.\d+)?)\s*(L|l|リットル|ℓ)/i, unit: "L" },
  // g, kg
  { regex: /(\d+(?:\.\d+)?)\s*(g|gr|グラム)/i, unit: "g" },
  { regex: /(\d+(?:\.\d+)?)\s*(kg|KG|キロ|キログラム)/i, unit: "kg" },
  // 個, 枚, 本, 袋, パック, 缶, 瓶, 箱
  { regex: /(\d+(?:\.\d+)?)\s*(個|コ|コ入|個入)/i, unit: "個" },
  { regex: /(\d+(?:\.\d+)?)\s*(枚|まい)/i, unit: "枚" },
  { regex: /(\d+(?:\.\d+)?)\s*(本|ほん|本入|本入り)/i, unit: "本" },
  { regex: /(\d+(?:\.\d+)?)\s*(袋|ふくろ|パック|p|P)/i, unit: "袋" },
  { regex: /(\d+(?:\.\d+)?)\s*(缶|カン|can)/i, unit: "缶" },
  { regex: /(\d+(?:\.\d+)?)\s*(瓶|ビン)/i, unit: "瓶" },
  { regex: /(\d+(?:\.\d+)?)\s*(箱|はこ)/i, unit: "箱" },
  // 日本語数字
  { regex: /([一二三四五六七八九十百千]+)\s*(個|枚|本|袋|パック|缶|瓶|箱)/i, unit: "個" },
];

/**
 * 商品名からカテゴリを推測する
 */
export function inferCategoryFromName(productName: string): {
  category: FoodCategory;
  confidence: number;
} {
  const normalizedName = productName.toLowerCase().replace(/\s+/g, "");
  
  for (const [category, keywords] of Object.entries(CATEGORY_KEYWORDS)) {
    for (const keyword of keywords) {
      const normalizedKeyword = keyword.toLowerCase().replace(/\s+/g, "");
      if (normalizedName.includes(normalizedKeyword)) {
        // 完全一致なら高信頼度、部分一致なら中信頼度
        const confidence = normalizedName === normalizedKeyword ? 0.95 : 0.85;
        return { category: category as FoodCategory, confidence };
      }
    }
  }
  
  return { category: "その他", confidence: 0.3 };
}

/**
 * 商品名から数量を抽出する
 */
export function extractQuantityFromName(productName: string): {
  amount: number | null;
  unit: string | null;
} {
  for (const pattern of QUANTITY_PATTERNS) {
    const match = productName.match(pattern.regex);
    if (match && match[1]) {
      const amount = parseFloat(match[1]);
      if (!isNaN(amount)) {
        return { amount, unit: pattern.unit };
      }
    }
  }
  
  return { amount: null, unit: null };
}

/**
 * 推奨賞味期限を計算する
 */
export function calculateRecommendedExpiry(
  category: FoodCategory,
  apiExpiryDate?: string | null
): Date {
  const now = new Date();
  
  // APIから期限が返ってきていればそれを優先
  if (apiExpiryDate) {
    const parsed = new Date(apiExpiryDate);
    if (!isNaN(parsed.getTime())) {
      return parsed;
    }
  }
  
  // カテゴリ別デフォルト日数を加算
  const days = CATEGORY_DEFAULT_DAYS[category] || 7;
  const expiry = new Date(now);
  expiry.setDate(expiry.getDate() + days);
  return expiry;
}
